    turtle.forward(95)
    turtle.penup()
    turtle.right(180)
    turtle.forward(47.5)
    turtle.left(90)
    turtle.pendown()
    turtle.forward(100)
    turtle.penup()
    turtle.right(180)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    return
